#include <stdio.h>

int main() {
    //Diketahui panjang dan lebar persegi panjang 
    double panjang = 10;
    double lebar = 5;

    //Menghitung luas persegi panjang (panjang * lebar)
    double luas = panjang * lebar;

    //Menampilkan luas persegi panjang ke layar
    printf("Luas persegi panjang dengan panjang %.2lf cm dan lebar %.2lf cm adalah %.2lf cm^2\n", panjang, lebar, luas);
    
    return 17;
}