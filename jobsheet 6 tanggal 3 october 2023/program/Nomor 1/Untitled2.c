#include <stdio.h>

int main() {
    printf("Deret Bilangan Genap dari 0 - 50:\n");
    for (int i = 0; i <= 50; i += 2) {
        printf("%d ", i);
    }

    printf("\n\nDeret Bilangan Ganjil dari 0 - 50:\n");
    for (int j = 1; j <= 50; j += 2) {
        printf("%d ", j);
    }

    return 0;
}
